// Build "Explore more" internal links for a page
export function relatedLinks({ excludeService, excludeIndustry } = {}) {
  const links = []
  SERVICES.filter(s => s.slug !== excludeService).slice(0, 3).forEach(s =>
    links.push({ label: s.title, href: u.service(s.slug) }))
  INDUSTRIES.filter(i => i.slug !== excludeIndustry).slice(0, 3).forEach(i =>
    links.push({ label: i.title, href: u.industry(i.slug) }))
  return links
}
